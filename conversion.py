def acre_feet_to_m3(volume_acre_feet):
    """Converts volume in US acre-feet to SI m³."""
    volume_m3 = volume_acre_feet * 1_233.482
    return volume_m3

def cms_to_cfs(flowrate_cms):
    """Converts flowrate rate in cubic metres per second to cubic feet per second."""
    flowrate_cfs = flowrate_cms * 35.3146662
    return flowrate_cfs

